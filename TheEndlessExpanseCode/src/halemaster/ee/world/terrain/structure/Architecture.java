package halemaster.ee.world.terrain.structure;

import java.util.List;

/**
 * @name Architecture
 * 
 * @version 0.0.0
 * 
 * @date Jan 30, 2014
 */
public class Architecture 
{
  Size size;
  List<ChanceTile>[] layout;
  RoomSet floorPlan = null;

  public Size getSize ()
  {
    return size;
  }

  public void setSize (Size size)
  {
    this.size = size;
  }

  public List<ChanceTile>[] getLayout ()
  {
    return layout;
  }

  public void setLayout (List<ChanceTile>[] layout)
  {
    this.layout = layout;
  }

  public RoomSet getFloorPlan ()
  {
    return floorPlan;
  }

  public void setFloorPlan (RoomSet floorPlan)
  {
    this.floorPlan = floorPlan;
  }
}
