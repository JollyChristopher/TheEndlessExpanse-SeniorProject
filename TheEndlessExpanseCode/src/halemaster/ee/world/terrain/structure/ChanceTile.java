package halemaster.ee.world.terrain.structure;

/**
 * @name ChanceTile
 * 
 * @version 0.0.0
 * 
 * @date Jan 30, 2014
 */
public class ChanceTile 
{
  private float chance;
  private int tile;

  public float getChance ()
  {
    return chance;
  }

  public void setChance (float chance)
  {
    this.chance = chance;
  }

  public int getTile ()
  {
    return tile;
  }

  public void setTile (int tile)
  {
    this.tile = tile;
  }
}
