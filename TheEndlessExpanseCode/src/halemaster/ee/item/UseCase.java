package halemaster.ee.item;

import halemaster.ee.world.entity.UseAnimation;

/**
 * @name UseCase
 * 
 * @version 0.0.0
 * 
 * @date Mar 23, 2014
 */
public class UseCase 
{
  private String target;
  private boolean useUp;
  private int useWait;
  private String cooldown;
  private StatModifier use;
  private float chance;
  private UseAnimation animation;

  public String getTarget ()
  {
    return target;
  }

  public boolean isUseUp ()
  {
    return useUp;
  }

  public int getUseWait ()
  {
    return useWait;
  }

  public String getCooldown ()
  {
    return cooldown;
  }

  public StatModifier getUse ()
  {
    return use;
  }

  public float getChance ()
  {
    return chance;
  }

  public UseAnimation getAnimation ()
  {
    return animation;
  }
}
